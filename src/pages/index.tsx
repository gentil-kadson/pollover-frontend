import type { InferGetServerSidePropsType, GetServerSideProps } from "next";

import Head from "next/head";
import styles from "../styles/Home.module.css";
import Link from "next/link";
import api from "./api/axios";

import PollCard from "@/components/PollCard";

type Poll = {
  id: number;
  title: string;
  total_votes: number;
};

export const getServerSideProps = (async () => {
  const { data } = await api.get("/polls");
  const polls: Poll[] = data;

  return { props: { polls } };
}) satisfies GetServerSideProps<{ polls: Poll[] }>;

export default function Home({
  polls,
}: InferGetServerSidePropsType<typeof getServerSideProps>) {
  console.log("olha a poll");
  console.log(polls);

  return (
    <>
      <Head>
        <title>Home</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <section className={styles.mainSection}>
          <h1 className={styles.title}>Running Polls</h1>
          <section className={styles.createPollSection}>
            <Link href="/create-poll">
              <button className={styles.button}>Create poll</button>
            </Link>
          </section>
          <div className={styles.pollCardsContainer}>
            {polls.map((poll) => (
              <PollCard
                key={poll.id}
                pollTitle={poll.title}
                totalVotes={poll.total_votes}
              />
            ))}
          </div>
        </section>
      </main>
    </>
  );
}
